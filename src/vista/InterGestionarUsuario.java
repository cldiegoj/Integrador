    private void CargarRol() {
        Connection cn = ConexionSQL.Conectar.getConexion();
        String sql = "select * from cargo";
        Statement st;

        try {

            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            cbo_cargo.removeAllItems();
            cbo_cargo.addItem("Seleccione Cargo:");
            while (rs.next()) {
                cbo_cargo.addItem(rs.getString("car_nom"));
            }
            cn.close();

        } catch (SQLException e) {
            System.out.println("Error al cargar categorias");
        }
    }
    private void CargarTablaUsuarios() {
        Connection con = ConexionSQL.Conectar.getConexion();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "select * from USUARIO";
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InterGestionarUsuario.jTable_usuarios = new JTable(model);
            InterGestionarUsuario.jScrollPane1.setViewportView(InterGestionarUsuario.jTable_usuarios);

            model.addColumn("CÓDIGO");//ID
            model.addColumn("USUARIOS");
            model.addColumn("CONTRASENA");
            model.addColumn("NOMBRES");
            model.addColumn("APELLIDOS");
            model.addColumn("TELEFONO");
            model.addColumn("ESTADO");

            while (rs.next()) {
                Object fila[] = new Object[7];
                for (int i = 0; i < 7; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla usuarios: " + e);
        }

        jTable_usuarios.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_usuarios.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idUsuario = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosUsuarioSeleccionado(idUsuario);//metodo
                }
            }
        });
    }



    /*
     * **************************************************
     * Metodo que envia datos seleccionados
     * **************************************************
     */
    private void EnviarDatosUsuarioSeleccionado(int idUsuario) {
        try {
            Connection con = ConexionSQL.Conectar.getConexion();
            PreparedStatement pst = con.prepareStatement(
                    "select * from USUARIO where usr_cod = '" + idUsuario + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                txt_usr_name.setText(rs.getString("usr_name"));
                txt_usr_pass.setText(rs.getString("usr_pass"));
                txt_usr_nom.setText(rs.getString("usr_nom"));
                txt_usr_ape.setText(rs.getString("usr_ape"));
                txt_usr_telf.setText(rs.getString("usr_telf"));
                cbo_cargo.setSelectedIndex(rs.getInt("car_cod"));
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al seleccionar usuario: " + e);
        }
    }



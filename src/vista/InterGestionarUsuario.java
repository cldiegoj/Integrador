    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
   
        Usuario usuario = new Usuario();
        UsuarioDAO usuariodao = new UsuarioDAO();

    if (idUsuario == 0) {
        JOptionPane.showMessageDialog(null, "¡Seleccione un Usuario!");
    } else {
        if (txt_usr_name.getText().isEmpty() || txt_usr_pass.getText().isEmpty() || txt_usr_nom.getText().isEmpty()
                || txt_usr_ape.getText().isEmpty() || txt_usr_telf.getText().isEmpty() || cbo_cargo.getSelectedItem().equals(null)) {
            JOptionPane.showMessageDialog(null, "¡Completa todos los campos!");

        } else {
            usuario.setUsr_name(txt_usr_name.getText().trim());
            usuario.setUsr_pass(txt_usr_pass.getText().trim());
            usuario.setUsr_nom(txt_usr_nom.getText().trim());
            usuario.setUsr_ape(txt_usr_ape.getText().trim());
            usuario.setUsr_telf(txt_usr_telf.getText().trim());
            usuario.setUsr_status(1);
            usuario.setCar_cod(cbo_cargo.getSelectedIndex());

            if(usuariodao.actualizar(usuario, idUsuario)){
                JOptionPane.showMessageDialog(null, "¡Actualización Exitosa!");
                this.Limpiar();
                this.CargarTablaUsuarios();
                idUsuario = 0;

            }else{
                JOptionPane.showMessageDialog(null, "¡Error al Actualizar usuario!");
            }
        }
    }

    }//GEN-LAST:event_jButton_actualizarActionPerformed

    private void jButton_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eliminarActionPerformed

        UsuarioDAO usuariodao = new UsuarioDAO();
        if (idUsuario == 0) {
            JOptionPane.showMessageDialog(null, "¡Seleccione un usuario!");
        } else {
            if (!usuariodao.eliminar(idUsuario)) {
                JOptionPane.showMessageDialog(null, "¡Usuario Eliminado!");
                this.CargarTablaUsuarios();
                this.Limpiar();
                idUsuario = 0;
            } else {
                JOptionPane.showMessageDialog(null, "¡Error al eliminar usuario!");
                this.Limpiar();
            }
        }

    }//GEN-LAST:event_jButton_eliminarActionPerformed

